# Variables
# Para python, todo es un objeto.
# Las variables son contenedores para almacenar valores.
# Las variables se definen usando el símbolo =.
# Ejemplo:
# x = 10
# pi = 3.14
# name = "Edwin"
# is_dev = True

# Ejemplo de variables:
x = 10        # int
pi = 3.14     # float
name = "Edwin"  # str
is_dev = True  # bool
# Nos damos cuenta que en python el tipo de dato se define por el valor que se le asigna a la variable.
# Esto es muy importante, ya que en otros lenguajes de programación, el tipo de dato se define por la variable.
# Ejemplo:
# x = 10 # int
# x = "10" # str
# x = 10.0 # float
# x = True # bool
# x = False # bool

print(x)
print(pi)
print(name)
print(is_dev)

# Más adelantes veremos clases, pero por ahora, solo veremos el tipo de dato de la variable.
# type() es una función nativa de Python que devuelve el tipo de dato de la variable.
print(type(x))
print(type(pi))
print(type(name))
print(type(is_dev))

# Aprendemos sobre el tipos de dato, la función nativa type() y sobre todo como "Imprimir" en consola.
# Imprimir en la consola es muy importante, ya que es la forma en que se comunica con el usuario que sucede en tu programa.

# y para no perder la costumbre, vamos a imprimir un mensaje en la consola.
print("Hola, mundo!")

